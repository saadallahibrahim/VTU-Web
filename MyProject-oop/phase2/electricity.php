  // Function to populate the meter type options based on the selected electricity company
  function setElectricityCompany(selectElement) {
    const selectedCompany = selectElement.value;
    const meterTypeSelect = document.getElementById("meter_type");

    // Clear existing options
    meterTypeSelect.innerHTML = '';

    // Add new options based on the selected electricity company
    if (selectedCompany === "Abuja (AEDC)" || selectedCompany === "Benin (BEDC)") {
      const prepaidOption = document.createElement("option");
      prepaidOption.value = "Prepaid";
      prepaidOption.textContent = "Prepaid";
      meterTypeSelect.appendChild(prepaidOption);

      const postpaidOption = document.createElement("option");
      postpaidOption.value = "Postpaid";
      postpaidOption.textContent = "Postpaid";
      meterTypeSelect.appendChild(postpaidOption);
    } else {
      const defaultOption = document.createElement("option");
      defaultOption.value = "";
      defaultOption.textContent = "Choose meter type";
      meterTypeSelect.appendChild(defaultOption);
    }
  }

  function validateForm() {
    const form = document.querySelector("form");
    const electricitySelect = form.elements["electricity"];
    const meterTypeSelect = form.elements["meter_type"];
    const meterNumberInput = form.elements["meter_number"];
    const amountInput = form.elements["amount"];

    if (electricitySelect.value === "") {
      alert("Please choose an electricity company.");
      return false;
    }

    if (meterTypeSelect.value === "") {
      alert("Please choose a meter type.");
      return false;
    }

    if (meterNumberInput.value.trim() === "") {
      alert("Please enter a meter number.");
      return false;
    }

    if (amountInput.value.trim() === "" || isNaN(amountInput.value)) {
      alert("Please enter a valid amount.");
      return false;
    }

    return true; // Form is valid and can be submitted
  }

  document.querySelector("form").addEventListener("submit", function (event) {
    if (!validateForm()) {
      event.preventDefault(); // Prevent the form from being submitted
    }
  });


</script>
